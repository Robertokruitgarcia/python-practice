# Variabelen en datatypes

# Variabelen
getal1 = 10
getal2 = 5

# Optellen
som = getal1 + getal2
print("De som van", getal1, "en", getal2, "is:", som)

# Aftrekken
verschil = getal1 - getal2
print("Het verschil tussen", getal1, "en", getal2, "is:", verschil)

# Vermenigvuldigen
product = getal1 * getal2
print("Het product van", getal1, "en", getal2, "is:", product)

# Delen
quotient = getal1 / getal2
print("Het quotient van", getal1, "en", getal2, "is:", quotient)